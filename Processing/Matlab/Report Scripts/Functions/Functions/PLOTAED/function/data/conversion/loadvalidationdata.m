function val = loadvalidationdata(fullpath,userdata,siteID,variableID,site,var)
% Function to ....
if exist('val','var') == 0
    load([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/epa.mat']);
    load([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/waterscope.mat']);
	load([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/Online.mat']);
    load([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/rivermurray.mat']);
    
    val.EPA = EPA;
    val.RM = RiverMurray;
    val.WS = WS;
	val.OL = Online;
end
% Special Case for DWL
dwlID = site.([cell2mat(userdata.simulation(1).project)]).DWL(siteID);
if exist([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/DWL/',...
        upper(dwlID{1}),'_',upper(variableID{1}),'.mat'],'file') > 0
    load([fullpath.datapth,'/',...
        cell2mat(userdata.simulation(1).project),'/mat/DWL/',...
        upper(dwlID{1}),'_',upper(variableID{1}),'.mat']);
    val.DWL = DWL;
end

